/** For-In 循环 */
//你可以使用 for-in 循环来遍历一个集合中的所有元素，例如数组中的元素、数字范围或者字符串中的字符。
//以下例子使用 for-in 遍历一个数组所有元素：

let names = ["Anna", "Alex", "Brian", "Jack"]
for name in names {
    print("Hello, \(name)!")
}
// Hello, Anna!
// Hello, Alex!
// Hello, Brian!
// Hello, Jack!

